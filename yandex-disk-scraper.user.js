(async function() {
  'use strict';
  
  console.log('[yds]: Юзерскрипт загружен');
  
  // map selector -> callback(element)
  const WATCHES = [
    { selector: '.resources-action-bar__body', onAdd: el => addButtonInActionBar(el) },
    { selector: '.slider__toolbar', onAdd: el => addButtonInSliderToolbar(el) },
  ];
  const THROTTLE_MS = 120; // coalesce frequent mutation batches

  // simple throttle to run last call after ms
  function throttle(fn, ms) {
    let timer = null;
    let lastArgs = null;
    return function (...args) {
      lastArgs = args;
      if (timer) return;
      timer = setTimeout(() => {
        timer = null;
        fn(...lastArgs);
      }, ms);
    };
  }

  // mark elements already processed to avoid duplicate onAdd calls
  const processed = new WeakSet();

  function handleFound(els, onAdd) {
    for (const el of els) {
      if (!processed.has(el)) {
        processed.add(el);
        try {
          onAdd(el);
        } catch (e) {
          console.error('[yds@handleFound]: onAdd callback error', e);
        }
      }
    }
  }

  // initial scan
  for (const w of WATCHES) {
    const found = Array.from(document.querySelectorAll(w.selector));
    if (found.length) {
      handleFound(found, w.onAdd);
    }
  }

  // helper: find matches inside a node (including the node itself)
  function findMatchesInNode(node, selector) {
    const out = [];
    if (node.nodeType !== Node.ELEMENT_NODE) {
      return out;
    }
    if (node.matches(selector)) {
      out.push(node);
    }
    node.querySelectorAll && node.querySelectorAll(selector).forEach(e => out.push(e));
    return out;
  }

  // MutationObserver callback (throttled)
  const onMutations = throttle((mutations) => {
    // collect roots changed to avoid repeated traversal
    const changedNodes = new Set();
    for (const m of mutations) {
      if (m.type === 'childList') {
        m.addedNodes.forEach(n => changedNodes.add(n));
      } else if (m.type === 'attributes' && m.target) {
        // attribute changes can make an element match; treat target as changed
        changedNodes.add(m.target);
      }
    }
    if (!changedNodes.size) {
      return;
    }

    for (const w of WATCHES) {
      const newly = [];
      for (const node of changedNodes) {
        findMatchesInNode(node, w.selector).forEach(el => newly.push(el));
      }
      if (newly.length) {
        handleFound(newly, w.onAdd);
      }
    }
  }, THROTTLE_MS);

  const observer = new MutationObserver(onMutations);
  observer.observe(document.documentElement, {
    childList: true,
    subtree: true,
    attributes: true, // set true only if attribute changes can cause selector matches
    attributeFilter: undefined
  });

  // expose stop function if needed
  window.__stopMultiSelectorWatcher = () => observer.disconnect();
  
  addStatus();
  
  // Additional detector of action bar (when switching files)
  document.body.addEventListener("mouseup", function() {
    let el = document.querySelector('.resources-action-bar__side-left');
    if (el) {
      setTimeout(() => addButtonInActionBar(el), 100);
    }
  });

  // OLDER
  
  const url = window.location.href;

  if (url.includes("/showcaptcha?")) {
    return;
  }

  // download button in sidebar
  if (url.startsWith("https://docs.")) {
    setTimeout(addDownloadButtonInSidebar, 200);
    setTimeout(async () => {
      if (await GM.getValue("autoDownload", false)) {
        openDocviewerAndStartDownload();
      }
    }, 1000);
    return;
  }

  // download pdf
  if (url.startsWith("https://docviewer.") && window.self === window.top) {
    if (document.title.endsWith(".txt")) {
      setTimeout(addDownloadTxtButton, 2000);
    } else {
      var info = await getFileInfo();
    	downloadPdf(info);
    }
  }
})();

