async function downloadVideo(m3u8Url, filename) {
  if (!m3u8Url) {
    console.error('[yds@downloadVideo]: No m3u8 URL');
    return false;
  }
  
  console.log('[yds@downloadVideo]: Start downloading video...', m3u8Url);
  
  const segments = await parsem3u8(m3u8Url);
  const totalSegments = segments.length;
  if (totalSegments === 0) {
    console.error('[yds@downloadVideo]: No segments found!');
    return false;
  }
  console.log('[yds@downloadVideo]: Found ' + totalSegments + ' segments');
  
  const buffer = await downloadVideoSegments(segments, filename);
  if (buffer === null) {
    console.error('[yds@downloadVideo]: Unable to download segments!');
    return false;
  }
  
  console.log('[yds@downloadVideo]: Writing input file...');
  updateStatus(filename, 'Сохранение файла...');
  const blob = new Blob([buffer], { type: 'video/' + filename.substring(filename.lastIndexOf('.') + 1, filename.length) });
  
  console.log('[yds@downloadVideo]: Triggering download...');
  const a = document.createElement('a');
  a.href = URL.createObjectURL(blob);
  a.download = filename;
  document.body.appendChild(a); a.click(); a.remove();
  URL.revokeObjectURL(a.href);
  
  console.log('[yds@downloadVideo]: Done!');
  updateStatus(filename, 'Готово!');
  return true;
}

async function parsem3u8(url) {
  const contents = await downloadText(url);
  if (contents === null) {
    console.error('[yds@parsem3u8]: Unable to get m3u8 contents', url);
    return [];
  }
  const base = url.substring(0, url.lastIndexOf('/') + 1);
  
  return contents
    .split('\n')
    .map(s => s.trim())
    .filter(l => l.includes('.ts?'))
  	.map(s => base + s);
}

async function downloadText(url) {
  return await fetch(url)
  	.then(response => {
    	if (!response.ok) {
        console.error('[yds@downloadText]: Unable to download text', url, response);
        return null;
      }
    	return response.text();
  	});
}

async function downloadVideoSegments(segments, filename) {
  const parts = [];
  const totalSegments = segments.length;
  let totalBytes = 0;
  let threads = [];
  let failed = false;
  let downloadedSegments = 0;
  updateProgressMax(filename, totalSegments);
  
  for (let currentSegment = 0; currentSegment < totalSegments; currentSegment++) {
    threads.push(
      fetch(segments[currentSegment])
      	.then(response => {
          if (!response.ok) {
            console.error(`[yds@downloadVideoSegments]: Unable to download segment ${currentSegment}/${totalSegments}`);
            failed = true;
            return null;
          }
          downloadedSegments++;
          console.log(`[yds@downloadVideoSegments]: ${downloadedSegments}/${totalSegments} downloaded`);
          updateStatus(filename, `Скачивание (${Math.round(downloadedSegments / totalSegments * 100)}%)`);
          updateProgressValue(filename, downloadedSegments);
          return response.arrayBuffer();
        })
    );
  }
  
  return await Promise.all(threads)
    .then(async (arrayBuffers) => {
      if (failed && window.confirm("Некоторые сегменты не удалось скачать. Итоговое видео может быть битым. Попробовать скачать заново?")) {
        window.location.reload();
      }

      console.log('[yds@downloadVideoSegments]: Saving downloaded segments...');
    	updateStatus(filename, 'Завершение загрузки...');

      for (const ab of arrayBuffers) {
        parts.push(new Uint8Array(ab));
        totalBytes += ab.byteLength;
      }
    })
    .then(() => {
    	const out = new Uint8Array(totalBytes);
      let off = 0;
      for (const p of parts) {
        out.set(p, off);
        off += p.length;
      }
      console.log('[yds@downloadVideoSegments]: All segments downloaded, total size: ' + totalBytes);
    	return out;
    });
}

function addToQueue(filename, format) {
  let queue = document.getElementById('yds-downloads');
  
  let item = document.createElement('li');
  item.id = btoa(filename).replaceAll('=', '_');
  item.style = "margin-bottom: 8px;";
  item.innerHTML = `<span>${filename} @ ${format}</span><progress style="margin: 0 8px 0 8px;"></progress><span class="yds-status">Начало скачивания...</span>`;
  
  queue.appendChild(item);
  queue.style.display = "";
}

function updateStatus(filename, message) {
  let id = btoa(filename).replaceAll('=', '_');
  document.querySelector(`#${id} .yds-status`).innerText = message;
}

function updateProgressMax(filename, max) {
  let id = btoa(filename).replaceAll('=', '_');
  let p = document.querySelector(`#${id} progress`).max = max;
}

function updateProgressValue(filename, value) {
  let id = btoa(filename).replaceAll('=', '_');
  let p = document.querySelector(`#${id} progress`).value = value;
}

function removeFromQueue(filename) {
  let queue = document.getElementById('yds-downloads');
  let id = btoa(filename).replaceAll('=', '_');
  document.querySelector(`#${id}`).remove();
  if (queue.children.length == 0) {
    queue.style.display = "none";
  }
}
